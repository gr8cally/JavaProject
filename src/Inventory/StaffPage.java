    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        if(editIdField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No ID Inserted", "WARNING", HEIGHT);
            return;
        }
        int id = Integer.valueOf(editIdField.getText());
        
        boolean exist = new CentralProvider().populateStaff(fnameField, lnameField, designationField, emailField, id);
        if(! exist){
            JOptionPane.showMessageDialog(null, id + " ID does not exist", "WARNING", HEIGHT);
        }
        else{
            confirmEditButton.setEnabled(true);
        }
        
    }//GEN-LAST:event_editButtonActionPerformed

