    private void confirmEditButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmEditButtonActionPerformed
        // TODO add your handling code here:
        
        if(fnameField.getText().isEmpty() || lnameField.getText().isEmpty() || designationField.getText().isEmpty() || emailField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Required Field is empty", "WARNING", HEIGHT);
            return;
        }
        int id = Integer.valueOf(editIdField.getText());
        
        boolean sucess = new CentralProvider().updateStaff(fnameField, lnameField, designationField, emailField, id);
        
        if(sucess){
            JOptionPane.showMessageDialog(null, "Update  Successful", "SUCCESS", HEIGHT);
            String query = " SELECT `staff_id`, `firstname`, `lastname`, `email`, `designation` FROM `login`";
        new CentralProvider().FillTable(staffTable, query);
        }
        else{
            JOptionPane.showMessageDialog(null, "Update Failed", "WARNING", HEIGHT);
        }
        confirmEditButton.setEnabled(false);
        fnameField.setText("");
        lnameField.setText("");
        designationField.setText("");
        emailField.setText("");
        editIdField.setText("");

    }//GEN-LAST:event_confirmEditButtonActionPerformed

